# learn data types in python
a = "Hello"
print(type(a))

a = 21
print(type(a))

a = 21.21
print(type(a))

a = 21j
print(type(a))

a = ["a",'b']
print(type(a))

a = ("a","b","a","c")
print(a)
print(type(a))

a = {"a":1, "b" :2}
print(type(a))

a = {"a","b","c","a"}
print(a)
print(type(a))

a = True
print(type(a))

a = b"Hello"
print(a)
print(type(a))

a = bytearray(5)
print(a)
print(type(a))

a = memoryview(bytes(5))
print(a)
print(type(a))

a = None
print(type(a))
